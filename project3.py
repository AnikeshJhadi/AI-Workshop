set1 ={1,2,3,4,5}
set2 ={1,3,5,7}
z=set1.union(set2)
print("Union of Set 1 and Set 2:-"+str(z))
z=set1.intersection(set2)
print("Intersection of Set 1 and Set 2:-"+str(z))
z=set1.difference(set2)
print("Difference of Set 1 and Set 2:-"+str(z))
z=set1.symmetric_difference(set2)
print("Symmetric Difference of Set 1 and Set 2:-"+str(z))
